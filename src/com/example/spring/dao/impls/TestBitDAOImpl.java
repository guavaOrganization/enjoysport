	@Autowired
	public void setDataSource(DataSource dataSource) {// 需要在xml上下文配置中配置<context:component-scan/>
		if (this.jdbcTemplate == null || dataSource != this.jdbcTemplate.getDataSource()) {
			this.jdbcTemplate = new JdbcTemplate(dataSource);
		}
		if (this.namedParameterJdbcTemplate == null) {
			this.namedParameterJdbcTemplate = new NamedParameterJdbcTemplate(dataSource);
		}
		// usingColumns 方法的参数是一个数组，可以指定允许插入的列名
		this.insertUser = new SimpleJdbcInsert(dataSource)
				.withTableName("t_user").usingColumns("name")
				.usingGeneratedKeyColumns("id");
		
		this.jdbcTemplate.setResultsMapCaseInsensitive(true);// 返回结果map对应的key忽略大小写，
		this.jdbcCall = new SimpleJdbcCall(jdbcTemplate).withProcedureName("read_user");
		
		this.testBitUserMappingSqlQuery = new TestBitUserMappingSqlQuery(dataSource);
	}
	
