	public TestBitUser getUserById(int id) throws DataAccessException {
		TestBitUser bitUser = this.jdbcTemplate.queryForObject("select * from t_user where id = ?", new Object[] { id },
				new RowMapper<TestBitUser>() {
					@Override
					public TestBitUser mapRow(ResultSet rs, int paramInt) throws SQLException {
						TestBitUser user = new TestBitUser();
						user.setId(rs.getInt("id"));
						user.setName(rs.getString("name"));
						return user;
					}
				});
		return bitUser;
	}
	
	public List<TestBitUser> getAllUser() throws DataAccessException{
		List<TestBitUser> bitUsers = this.jdbcTemplate.query("select * from t_user", new TestBitUserMapper());
		return bitUsers;
	}
	
	private static final class TestBitUserMapper implements RowMapper<TestBitUser> {// 可以将此部分提取出来当作一个公共类
		@Override
		public TestBitUser mapRow(ResultSet rs, int rowNum) throws SQLException {
			TestBitUser user = new TestBitUser();
			user.setId(rs.getInt("id"));
			user.setName(rs.getString("name"));
			return user;
		}
	}
	
	public int[] batchUpdateUser(final List<TestBitUser> bitUsers) throws DataAccessException{
		String updateSQL = "update t_user set name = ? where id = ?";
		int[] updateCounts = this.jdbcTemplate.batchUpdate(updateSQL,new BatchPreparedStatementSetter() {
			@Override
			public void setValues(PreparedStatement ps, int i) throws SQLException {
				ps.setString(1, bitUsers.get(i).getName());
				ps.setInt(2, bitUsers.get(i).getId());
			}
			@Override
			public int getBatchSize() {
				return bitUsers.size();
			}
		});
		return updateCounts;
	}
	
	// 使用命名空间方式
	public int[] batchUpdateUserWithNamedParameterJdbcTemplate(List<TestBitUser> bitUsers) throws DataAccessException{
		String updateSQL = "update t_user set name = :name where id = :id";
		SqlParameterSource[] batch = SqlParameterSourceUtils.createBatch(bitUsers.toArray());
		int[] updateCounts = this.namedParameterJdbcTemplate.batchUpdate(updateSQL, batch);
		return updateCounts;
	}
	
	// 使用问号方式
	public int[] batchUpdateUserWithQuestionMark(List<TestBitUser> bitUsers) throws DataAccessException {
		String updateSQL = "update t_user set name = ? where id = ?";
		List<Object[]> batch = new ArrayList<Object[]>();
		for (TestBitUser bitUser : bitUsers) {
			// 数组中参数的顺序一定要与"?"出现的顺序保持一致
			Object[] values = new Object[] { bitUser.getName(), bitUser.getId() };
			batch.add(values);
		}
		int[] updateCounts = this.jdbcTemplate.batchUpdate(updateSQL, batch);
		return updateCounts;
	}
