	// 可以使用update方法完成"insert"、"update"和"delete"操作
	public void insertToUser(int id, String name) throws DataAccessException {
		this.jdbcTemplate.update("insert into t_user(name) values(?)", name);
	}
	
	public int insertToUserAndRetrievingAutoGeneratedKey(final String name) throws DataAccessException {
		final String INSERT_SQL = "insert into t_user(name) values(?)";
		KeyHolder keyHolder = new GeneratedKeyHolder();
		this.jdbcTemplate.update(new PreparedStatementCreator() {
			@Override
			public PreparedStatement createPreparedStatement(Connection connection) throws SQLException {
				PreparedStatement ps = connection.prepareStatement(INSERT_SQL, new String[] { "id" });
				ps.setString(1, name);
				return ps;
			}
		}, keyHolder);
		return (int) keyHolder.getKey();
	}
	
